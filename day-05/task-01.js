// 5টা fruits এর array বানাও
var fruits = ["apple", "banana", "orange", "grape", "mango"];
console.log(fruits);

// 2nd element print করো
console.log(fruits[1]);

// last element print করো
console.log(fruits[fruits.length -1]);

// নতুন একটি fruit add করো (push)
fruits.push("kiwi");
console.log(fruits);

// first element remove করো (shift)
fruits.shift();
console.log(fruits);