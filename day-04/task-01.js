// একটি function বানাও square(num) → number square return করবে

function square(num){
    return num*num;
}

console.log(square(4));

// একটি function বানাও isEven(num) → even হলে true return করবে
function isEven(num){
    return num%2 !=0;
}

console.log(isEven(5));

